package me.remigio07.chatplugin.api.common.util.packet;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * Annotation used in {@link Packets} to indicate that a packet
 * is to be read/sent through the custom socket channel and not
 * through the standard BungeeCord's plugin messaging channel.
 */
@Target(ElementType.METHOD)
@Retention(RetentionPolicy.SOURCE)
public @interface SocketChannelPacket {
	
}
